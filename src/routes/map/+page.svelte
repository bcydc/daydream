<script lang="ts">
	import { onMount } from 'svelte';
	import { browser } from '$app/environment';
	
	/** @type {import('./$types').PageData} */
	export let data;
	
	let mapContainer: HTMLElement;
	let map: any;
	
	onMount(() => {
		if (browser) {
			// Initialize Leaflet
			import('leaflet').then((L) => {
				// Load CSS
				const link = document.createElement('link');
				link.rel = 'stylesheet';
				link.href = 'https://unpkg.com/leaflet@1.9.4/dist/leaflet.css';
				document.head.appendChild(link);
				
				// Initialize map
				map = L.map(mapContainer, {
					minZoom: 2,
					maxZoom: 18
				}).setView([20, 0], 2);
				
				// Add tile layer with custom styling
				L.tileLayer('https://{s}.basemaps.cartocdn.com/light_all/{z}/{x}/{y}{r}.png', {
					attribution: '© OpenStreetMap contributors © CARTO',
					detectRetina: true
				}).addTo(map);
				
				// Create custom icon using map-flag.png
				const flagIcon = L.icon({
					iconUrl: '/daydream-media/map-flag.png',
					iconSize: [32, 32],
					iconAnchor: [16, 32],
					popupAnchor: [0, -32]
				});
				
				// Add markers for each location (data is prerendered)
				data.locations.forEach((location: any) => {
					if (location.lat && location.lng) {
						const marker = L.marker([location.lat, location.lng], { icon: flagIcon })
							.addTo(map);
						
						// Create popup that shows on hover
						const popup = L.popup({
							closeButton: false,
							className: 'custom-popup'
						}).setContent(location.event_name);
						
						marker.bindPopup(popup);
						
						// Show popup on hover, hide on mouseout
						marker.on('mouseover', () => {
							marker.openPopup();
						});
						marker.on('mouseout', () => {
							marker.closePopup();
						});
					}
				});
			});
		}
	});
</script>

<svelte:head>
